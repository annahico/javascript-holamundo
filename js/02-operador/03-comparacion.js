//AHORA SE UTILIZA LOS SIMBOLOS DE ABAJO, NO LOS DE ARRIBA
console.log(a == "10"); //igual al STRING de 10 el valor = pregunta los valores, NO los tipos
console.log(a === "10"); //ponemos triple = para consultar valor y tipo
